function escapeCSV(val: any): string {
  if (val === undefined || val === null) return '';
  const str = String(val);
  if (str.includes(',') || str.includes('"') || str.includes('\n')) {
    return `"${str.replace(/"/g, '""')}"`;
  }
  return str;
}

function getScoreColor(score: number): string {
  if (score >= 70) return '#d9534f'; // red
  if (score >= 35) return '#f0ad4e'; // yellow
  return '#5cb85c'; // green
}

function getScoreBackground(score: number): string {
  if (score >= 70) return '#fdf2f2'; // light red
  if (score >= 35) return '#fefaf3'; // light yellow
  return '#f2faf2'; // light green
}

function getScoreCategory(score: number): string {
  if (score >= 70) return 'HIGH EXPOSURE';
  if (score >= 35) return 'MODERATE EXPOSURE';
  return 'SECURE / LOW EXPOSURE';
}
