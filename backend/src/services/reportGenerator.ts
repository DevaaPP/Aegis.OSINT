export function generateJSONReport(scanData: any): string {
  return JSON.stringify(
    {
      reportName: 'Digital Footprint Exposure Report',
      target: scanData.target,
      scanType: scanData.type,
      riskScore: scanData.riskScore,
      scannedAt: new Date(scanData.createdAt).toISOString(),
      findingsCount: scanData.findings.length,
      findings: scanData.findings.map((f: any) => ({
        category: f.category,
        severity: f.severity,
        title: f.title,
        description: f.description,
        remediation: f.remediation
      }))
    },
    null,
    2
  );
}

export function generateCSVReport(scanData: any): string {
  const headers = ['Category', 'Severity', 'Title', 'Description', 'Remediation'];
  const rows = scanData.findings.map((f: any) => [
    escapeCSV(f.category),
    escapeCSV(f.severity),
    escapeCSV(f.title),
    escapeCSV(f.description),
    escapeCSV(f.remediation)
  ]);

  return [
    `Target,${escapeCSV(scanData.target)},Scan Type,${scanData.type},Risk Score,${scanData.riskScore}`,
    headers.join(','),
    ...rows.map((row: string[]) => row.join(','))
  ].join('\n');
}

